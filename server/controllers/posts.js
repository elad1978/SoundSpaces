/* UPDATE POSTS */

export const likePost = async (req, res) => {
  try {
    const { id } = req.params;
    const { userId } = req.body;
    const post = await Post.findById(id);
    const isLiked = post.likes.get(userId); //associated with the Map of the post object.

    if (isLiked) {
      post.likes.delete(userId); //deletes the key-value pair from the Map object
    } else {
      post.likes.set(userId, true); //sets the key-value pair in the Map object
    }

    const updatedPost = await Post.findByIdAndUpdate(
      id,
      { likes: post.likes },
      { new: true }
    );

    res.status(200).json(updatedPost);
  } catch (err) {
    res.status(404).json({ message: err.message });
  }
};
